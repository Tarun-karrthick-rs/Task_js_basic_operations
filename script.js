let num1=26;
let num2=13;

//from num1 to num2
console.log(num1+num2);
console.log(num1-num2);
console.log(num1/num2);
console.log(num1%num2);
console.log(num1**num2);


//from num2 to num1
console.log(num2+num1);
console.log(num2-num1);
console.log(num2/num1);
console.log(num2%num1);
console.log(num2**num1);


//post and pre increment 
console.log(num1++);
console.log(++num1);

//post and pre decrement 
console.log(num1--);
console.log(--num1);

console.log(~num1)